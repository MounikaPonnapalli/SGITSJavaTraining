//Display multiplication of numbers from 23 to 28
public class Table3 
{
    public static void main(String[] args)
    {
        for(int i=23;i<=28;i++)
        {
            for(int j=1;j<=10;j++)
            {
                System.out.println(i+"*"+j+"="+i*j);
            }
            System.out.println(" ");
        }
    }
}
