package Task4;

public class MultiplicationNumbers {
    public static void main(String[] args)
    {
        for(int i=23;i<=28;i++)
        {
            for(int j=1;j<=10;j++)
            {
                System.out.println(i+"*"+j+"="+i*j);
            }
            System.out.println(" ");
        }
    }
}    